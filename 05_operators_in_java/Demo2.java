public class Demo2 {
    public static void main(String[] args) {
        // Relational Operators
        // == != > < <= >=

        int a = 5;
        int b = 10;
        boolean c = (a == b);
        System.out.println(c);

        boolean d = (a != b);
        System.out.println(d);

        boolean e = (a < b);
        System.out.println(e);

        boolean f = (a > b);
        System.out.println(f);

        boolean g = (a <= b);
        System.out.println(g);

        boolean h = (a >= b);
        System.out.println(h);
    }
}
